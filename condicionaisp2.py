nome = input('Digite seu nome:').capitalize()
if nome == 'Milton':
   print(f'Bom dia, {nome.capitalize()}! Que nome bonito! ')
elif nome == 'João' or nome == 'Pedro' or nome == 'Tiago':
   print(f'Bom dia, {nome.capitalize()}! O seu nome é Biblíco!')
else:
   print(f'Bom dia, {nome.capitalize()}!')




